const RICH_COPY_CODE_STYLE_EXCEPTIONS = new Set([
  'display',
  'overflow',
  'overflow-x',
  'overflow-y',
]);

const RICH_COPY_ARTIFACT_STYLE_EXCEPTIONS = new Set([
  'display',
  'overflow',
  'overflow-x',
  'overflow-y',
]);

const shouldPreserveCodeSnippetStyle = (element: HTMLElement, prop: string) =>
  Boolean(element.closest('.code-snippet__fix')) && RICH_COPY_CODE_STYLE_EXCEPTIONS.has(prop);

const shouldPreserveRichArtifactStyle = (element: HTMLElement, prop: string) =>
  Boolean(element.closest('.rich-artifact__fix')) && RICH_COPY_ARTIFACT_STYLE_EXCEPTIONS.has(prop);

export const sanitizeRichCopyStyles = (root: HTMLElement) => {
  [root, ...Array.from(root.querySelectorAll<HTMLElement>('*'))].forEach((element) => {
    RICH_COPY_FORBIDDEN_STYLE_PROPS.forEach((prop) => {
      if (shouldPreserveCodeSnippetStyle(element, prop)) return;
      if (shouldPreserveRichArtifactStyle(element, prop)) return;
      element.style.removeProperty(prop);
    });
  });
};
